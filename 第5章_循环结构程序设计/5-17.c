/*
 * 习题17
 * 两个乒乓球队进行比赛，各出 3 人。甲队为 A, B, C 3 人，
 * 乙队为 X, Y, Z 3 人。已抽签决定比赛名单。
 * 有人向队员打听比赛的名单，A 说他不和 X 比，C 说他不和 X, Z 比，
 * 请编程序找出 3 对赛手的名单。
 *
 * 注：本章习题13～15所用的方法，可参考《C程序设计（第五版）学习辅导》
 *     第5章习题解答中的介绍。
*/

#include <stdio.h>

int main()
{
    char a, b, c;  // A、B、C 的对手（乙队选手）
    for (a = 'X'; a <= 'Z'; a++)         // 遍历 A 的对手
        for (b = 'X'; b <= 'Z'; b++)     // 遍历 B 的对手
            for (c = 'X'; c <= 'Z'; c++) // 遍历 C 的对手
            {
                if (a != b && a != c && b != c          // 一人一个对手
                    && a != 'X'                          // A 不和 X 比
                    && c != 'X' && c != 'Z')             // C 不和 X、Z 比
                {
                    printf("A vs %c\n", a);
                    printf("B vs %c\n", b);
                    printf("C vs %c\n", c);
                }
            }
    getchar();
    return 0;
}


