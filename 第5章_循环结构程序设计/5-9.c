/*
 * 习题9
 * 一个数如果恰好等于它的因子之和，这个数就称为"完数"。
 * 例如，6 的因子为 1, 2, 3，而 6 = 1 + 2 + 3，因此 6 是"完数"。
 * 编程找出 1000 之内的所有完数，并按下面格式输出其因子：
 *   6 its factors are 1,2,3
 */

#include <stdio.h>

int main()
{
    int i, j, sum;
    for (i = 2; i <= 1000; i++)
    {
        sum = 0;
        for (j = 1; j <= i / 2; j++)
            if (i % j == 0)
                sum += j;

        if (sum == i)
        {
            printf("%d its factors are 1", i);
            for (j = 2; j <= i / 2; j++)
                if (i % j == 0)
                    printf(",%d", j);
            printf("\n");
        }
    }
    getchar();
    return 0;
}
