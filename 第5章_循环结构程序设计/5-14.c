/*
 * 习题14
 * 用牛顿迭代法求下面方程在 1.5 附近的根：
 *     2x^3 - 4x^2 + 3x - 6 = 0
*/

#include <stdio.h>
#include <math.h>

int main()
{
    double x0, x1 = 1.5;
    do
    {
        x0 = x1;
        x1 = x0 - (2 * x0 * x0 * x0 - 4 * x0 * x0 + 3 * x0 - 6)
                  / (6 * x0 * x0 - 8 * x0 + 3);
    } while (fabs(x1 - x0) > 1e-5);
    printf("方程的根为：%.3f\n", x1);
    getchar();
    return 0;
}


