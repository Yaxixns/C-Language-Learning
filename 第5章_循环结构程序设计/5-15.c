/*
 * 习题15
 * 用二分法求下面方程在 (-10, 10) 的根：
 *     2x^3 - 4x^2 + 3x - 6 = 0
 */

#include <stdio.h>
#include <math.h>

int main()
{
    float left = -10, right = 10, x, fx;
    do
    {
        x = (left + right) / 2.0;
        fx = 2 * x * x * x - 4 * x * x + 3 * x - 6;
        if (fx < 0)
            left = x;     // f(x) < 0，根在右边，右移左边界
        else
            right = x;    // f(x) > 0，根在左边，左移右边界
    } while (fabs(fx) > 1e-5);
    printf("%.3f\n", x);
    getchar();
    return 0;
}

