/*
 * 习题5
 * 求 Sn = a + aa + aaa + ... + aa...a（n个a）之值，
 * 其中 a 是一个数字，n 表示 a 的位数，n 由键盘输入。
 * 例如：2 + 22 + 222 + 2222 + 22222 （此时 n=5）
 */

#include <stdio.h>
int main()
{
    int n, temp, i, a = 2, sum = 0;
    scanf("%d", &n);
    temp = a;
    for (i = 0; i < n; i++)
    {
        sum += temp;
        temp = temp * 10 + a;
    }

    printf("%d\n", sum);
    getchar();
    return 0;
}
