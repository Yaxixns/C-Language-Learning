/*
 * 习题8
 * 输出所有的"水仙花数"，所谓"水仙花数"是指一个 3 位数，
 * 其各位数字立方和等于该数本身。
 * 例如，153 是水仙花数，因为 153 = 1^3 + 5^3 + 3^3。
 */

#include <stdio.h>
int main()
{
    int i, ge, shi, bai;
    for (i = 100; i <= 999; i++)
    {
        bai = i / 100;           // 百位
        shi = i / 10 % 10;       // 十位
        ge  = i % 10;            // 个位

        if (bai * bai * bai + shi * shi * shi + ge * ge * ge == i)
            printf("%d\n", i);
    }
    getchar();
    return 0;
}

