/*
 * 习题12
 * 猴子吃桃问题。猴子第 1 天摘下若干个桃子，当即吃了一半，
 * 还不过瘾，又多吃了一个。第 2 天早上又将剩下的桃子吃掉一半，
 * 又多吃了一个。以后每天早上都吃了前一天剩下的一半零一个。
 * 到第 10 天早上想再吃时，就只剩一个桃子了。
 * 求第 1 天共摘多少个桃子。
 */

#include <stdio.h>

int main()
{
    int i, n = 1;
    for (i = 0; i < 9; i++)
    {
        n = (n + 1) * 2;
    }
    printf("第一题共摘%d个桃子\n", n);
    getchar();
    return 0;
}
