/*
 * 习题3
 * 输入两个正整数 m 和 n，求其最大公约数和最小公倍数。
 */

#include <stdio.h>

int main()
{
    int m, n, a, b, t, gcd, lcm;

    printf("请输入两个正整数 m 和 n: ");
    scanf("%d %d", &m, &n);

    a = m;
    b = n;

    /* 辗转相除法求最大公约数 */
    while (b != 0)
    {
        t = a % b;
        a = b;
        b = t;
    }
    gcd = a;

    /* 最小公倍数 = 两数乘积 / 最大公约数 */
    lcm = m * n / gcd;

    printf("最大公约数: %d\n", gcd);
    printf("最小公倍数: %d\n", lcm);

    return 0;
}
