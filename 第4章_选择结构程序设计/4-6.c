/*
 * 习题6
 * 有一函数：
 *         /  x      (x < 1)
 *   y =  |  2x - 1  (1 <= x < 10)
 *         \  3x - 11 (x >= 10)
 * 写程序，输入 x 的值，输出 y 相应的值。
 */

#include <stdio.h>

int main()
{
    int x, y;
    scanf("%d", &x);
    if (x < 1)
        y = x;
    else if (x < 10)
        y = 2 * x - 1;
    else
        y = 3 * x - 11;
    printf("y=%d\n", y);

    getchar();
    return 0;
}
/* ────────────────────────────────────────
 * 📝 批改评语：✅ 完全正确！10/10
 *
 * ① 三个区间划分准确：x<1、1≤x<10、x≥10。
 * ② else-if 链式写法优雅，条件不重叠、不遗漏。
 * ③ 边界处理正确：
 *    x=0  → x<1 → y=0 ✓
 *    x=1  → x<10(且≥1) → y=1 ✓  (2*1-1=1, 与第一段在x=1处不矛盾)
 *    x=10 → else → y=19 ✓
 *
 * 💡 这就是分段函数的标准写法，考试大概率会考。
 * ──────────────────────────────────────── */
