/*
 * 习题12
 * 有 4 个圆塔，圆心分别为 (2, 2), (-2, 2), (-2, -2), (2, -2)，圆半径为 1。
 * 这 4 个塔的高度为 10m，塔以外无建筑物。今输入任一点的坐标，
 * 求该点的建筑高度（塔外的高度为零）。
 */

#include <stdio.h>
#include <math.h>

int main()
{
    int h = 0;
    float x, y;

    printf("请输入坐标：");
    scanf("(%f,%f)", &x, &y);

    if (sqrt((x - 2) * (x - 2) + (y - 2) * (y - 2)) <= 1)
        h = 10;
    if (sqrt((x + 2) * (x + 2) + (y - 2) * (y - 2)) <= 1)
        h = 10;
    if (sqrt((x - 2) * (x - 2) + (y + 2) * (y + 2)) <= 1)
        h = 10;
    if (sqrt((x + 2) * (x + 2) + (y + 2) * (y + 2)) <= 1)
        h = 10;
    printf("%d\n", h);

    getchar();
    return 0;
}
/* ────────────────────────────────────────
 * 📝 批改评语：✅ 正确！10/10
 *
 * ① 四个塔的判断逻辑对：点在圆内 ← 距离 ≤ 半径。
 * ② sqrt距离公式正确，四个圆心坐标代入无误。
 * ③ h=0 默认值 + 四个if逐一覆盖，结果正确。
 *
 * 💡 优化建议（非扣分）：
 *   - 4个独立if会全部执行，用 else-if 链可以减少计算量
 *   - 或者比较平方距离 (dx²+dy²) <= 1，省去 sqrt 调用
 *     if ((x-2)*(x-2)+(y-2)*(y-2) <= 1) h=10;
 *     else if ...（可提前结束）
 *   - scanf("(%f,%f)",...) 要求输入带括号和逗号，如 (3,2)，
 *     改成 scanf("%f %f",...) 更友好。
 * ──────────────────────────────────────── */
