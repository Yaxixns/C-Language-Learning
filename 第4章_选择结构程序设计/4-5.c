/*
 * 习题5
 * 从键盘输入一个小于 1000 的正数，要求输出它的平方根（如平方根不是整数，
 * 则输出其整数部分）。要求在输入数据后先对其进行检查是否为小于 1000 的正数。
 * 若不是，则要求重新输入。
 */

#include <stdio.h>

int main()
{
    int d = 1001;
    scanf("%d", &d);
    while (d > 1000)
    {
        printf("要求d<1000\n");
        scanf("%d", &d);
    }
    int i;
    for (i = 1; i < 35; i++)
    {
        if (i * i > d)
            break;
    }
    printf("%d的平方根是%d", d, i - 1);
    getchar();
    return 0;
}
/* ────────────────────────────────────────
 * 📝 批改评语：⚠️ 逻辑有瑕疵！6/10
 *
 * ✅ 做得好的：
 * ① 平方根用循环逼近到整数部分，手动实现 sqrt 取整，思路不错。
 * ② while 循环实现了"重新输入"的需求。
 *
 * ⚠️ 问题：
 * ① 题目要求"小于 1000 的正数"——你只检查了 d>1000，没检查 d<=0。
 *    如果输入 -5 或 0，程序会直接去算平方根，不符合题意。
 *    正确条件：while (d >= 1000 || d <= 0)
 *    注意 1000 本身也不满足"小于1000"，应该用 >= 而非 >。
 *
 * ② int d = 1001 这个初始化很别扭，纯粹为了让第一次进 while。
 *    更好的写法是用 do-while：
 *      do { scanf("%d", &d); } while (d >= 1000 || d <= 0);
 *    do-while 至少执行一次，正是为这种场景设计的。
 *
 * ③ for(i=1; i<35; i++) 中的 35 是 magic number。
 *    更通用：i*i <= d 作为循环条件，d<1000 时 sqrt(d)<32。
 *
 * 📌 考试重点：边界值检查（< 还是 <=，> 还是 >=）+ 正数判断。
 * ──────────────────────────────────────── */
