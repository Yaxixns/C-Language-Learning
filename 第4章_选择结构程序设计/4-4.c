/*
 * 习题4
 * 有 3 个整数 a, b, c，由键盘输入，输出其中最大者。
 */

#include <stdio.h>

int main()
{
    int a, b, c, max;
    printf("请输入三个数：\n");
    scanf("%d %d %d", &a, &b, &c);
    max = a;
    if (b > max)
        max = b;
    if (c > max)
        max = c;
    printf("max = %d\n", max);

    getchar();
    return 0;
}
