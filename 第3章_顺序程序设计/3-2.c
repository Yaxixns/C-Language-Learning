/*
 * 习题2
 * 存款利息的计算。有 1000 元，想存 5 年，可按以下 5 种办法存：
 * （1）一次存 5 年期。
 * （2）先存 2 年期，到期后将本息再存 3 年期。
 * （3）先存 3 年期，到期后将本息再存 2 年期。
 * （4）存 1 年期，到期后将本息再存 1 年期，连续存 5 次。
 * （5）存活期存款。活期利息每一季度结算一次。
 *
 * 2017 年的银行存款利息如下：
 *   1 年期定期存款利息为 1.5%；
 *   2 年期定期存款利息为 2.1%；
 *   3 年期定期存款利息为 2.75%；
 *   5 年期定期存款利息为 3%；
 *   活期存款利息为 0.35%（活期存款每一季度结算一次利息）。
 *
 * 如果 r 为年利率，n 为存放年数，则计算本息和的公式如下：
 *   1 年期本息和：P = 1000 * (1 + r)
 *   n 年期本息和：P = 1000 * (1 + n * r)
 *   存 n 次 1 年期的本息和：P = 1000 * (1 + r)^n
 *   活期存款本息和：P = 1000 * (1 + r/4)^(4n)
 *
 * 说明：1000 * (1 + r/4) 是一个季度的本息和。
 */

#include <stdio.h>
#include <math.h>
#include <stdlib.h>

int main()
{
    system("chcp 65001 > nul"); // 终端切 UTF-8，解决乱码
    float bs = 1000;            // 本金
    float p1, p2, p3, p4, p5;

    // (1) 一次存5年期：P = 1000 * (1 + n * r)
    p1 = bs * (1 + 5 * 0.03);

    // (2) 先存2年，到期本息再存3年
    p2 = bs * (1 + 2 * 0.021) * (1 + 3 * 0.0275);

    // (3) 先存3年，到期本息再存2年（和(2)结果一样）
    p3 = bs * (1 + 3 * 0.0275) * (1 + 2 * 0.021);

    // (4) 存1年期，连续存5次：P = 1000 * (1 + r)^5
    p4 = bs * pow(1 + 0.015, 5);

    // (5) 活期，每季度结算：P = 1000 * (1 + r/4)^(4*5)
    p5 = bs * pow(1 + 0.0035 / 4, 4 * 5);

    printf("本金 1000 元，存 5 年\n");
    printf("================================\n");
    printf("方案(1) 一次存5年期  ：%.2f 元\n", p1);
    printf("方案(2) 2年+3年      ：%.2f 元\n", p2);
    printf("方案(3) 3年+2年      ：%.2f 元\n", p3);
    printf("方案(4) 1年×5次      ：%.2f 元\n", p4);
    printf("方案(5) 活期(季度结算)：%.2f 元\n", p5);

    getchar();
    return 0;
}

