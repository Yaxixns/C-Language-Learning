/*
 * 习题5
 * 用下面的 scanf 函数输入数据，使 a=3, b=7, x=8.5, y=71.82, c1='A', c2='a'。
 * 在键盘上应如何输入？
 *
 *   #include <stdio.h>
 *   int main()
 *   {
 *       int a, b;
 *       float x, y;
 *       char c1, c2;
 *       scanf("a=%db=%d", &a, &b);
 *       scanf("%f%e", &x, &y);
 *       scanf("%c%c", &c1, &c2);
 *       return 0;
 *   }
 */

#include <stdio.h>
int main()
{
    int a, b;
    float x, y;
    char c1, c2;
    scanf("a=%db=%d", &a, &b);
    scanf("%f%e", &x, &y);
    scanf("%c%c", &c1, &c2);
    getchar();
    return 0;
}

/*
    解答：
    a=3b=7 8.5 71.82Aa

    ┌─────────────────────────────────────────────────┐
    │ 注意事项                                        │
    ├─────────────────────────────────────────────────┤
    │ 1. 格式字符串中的普通字符必须原样输入              │
    │    scanf("a=%db=%d", ...) 中的 "a=" 和 "b="      │
    │    是普通字符，用户必须照原样键入 a=3b=7          │
    │                                                  │
    │ 2. %d、%f、%e 会自动跳过前导空白字符（空格、      │
    │    换行、Tab），然后读取数据                      │
    │                                                  │
    │ 3. %c 不会跳过空白字符！这是本题最大的坑：         │
    │    - 如果数字和字符之间有空格，空格会被当作字符     │
    │      读入 c1                                      │
    │    - 所以 "Aa" 必须紧跟在 71.82 后面，中间不能     │
    │      有空格、回车等任何字符                        │
    │                                                  │
    │ 4. 如果希望在 %c 前加空格跳过空白，可以写成        │
    │    scanf(" %c %c", &c1, &c2);                     │
    │    格式串中 %c 前的空格表示跳过所有空白字符        │
    └─────────────────────────────────────────────────┘
*/
