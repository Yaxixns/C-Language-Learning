/*
 * 习题4
 * 分析下面的程序：
 *   #include <stdio.h>
 *   int main()
 *   {
 *       char c1, c2;
 *       c1 = 97;
 *       c2 = 98;
 *       printf("c1=%c,c2=%c\n", c1, c2);
 *       printf("c1=%d,c2=%d\n", c1, c2);
 *       return 0;
 *   }
 *
 * （1）运行时会输出什么信息？为什么？
 *
 * （2）如果将程序第4、5行改为
 *       c1 = 197;
 *       c2 = 198;
 *     运行时会输出什么信息？为什么？
 *
 * （3）如果将程序第3行改为
 *       int c1, c2;
 *     运行时会输出什么信息？为什么？
 */

/*
(1)
输出：
c1=a,c2=b
c1=97,c2=98

原因：
字符型变量 c1、c2 分别被赋值为 97 和 98，而 97 和 98 分别是小写字母 'a' 和 'b' 的 ASCII 码。
- 第一个 printf 使用 %c 格式符，将 c1、c2 按字符输出，所以显示 a 和 b。
- 第二个 printf 使用 %d 格式符，将 c1、c2 按十进制整数输出，所以显示 97 和 98。

(2)
输出（假设 char 为有符号类型，取值范围 -128~127，大多数编译器如此）：
c1=?,c2=?（输出不可打印字符或扩展ASCII字符，因系统而异）
c1=-59,c2=-58

原因：
197 和 198 超出了 signed char 的取值范围（-128~127）。
- 197 的二进制表示为 11000101。作为有符号 char，最高位为 1，表示负数，其值为补码：
  取反 00111010，加 1 得 00111011 = 59，所以实际存储的值为 -59。
- 198 的二进制表示为 11000110。同理，取反加 1 得 00111010 = 58，实际存储的值为 -58。
- 第一个 printf 用 %c 输出 -59 和 -58 对应的字符。由于它们是负数，%c 会将其转换为 unsigned char
  （即 197 和 198），输出扩展 ASCII 表中对应位置的字符（具体显示内容取决于系统字符集）。
- 第二个 printf 用 %d 输出 -59 和 -58。

注：若 char 为无符号类型（取值范围 0~255），则 197 和 198 不溢出，输出为对应扩展 ASCII 字符及 197、198。

(3)
输出（仅将第 3 行 char 改为 int，保持 c1=97, c2=98）：
c1=a,c2=b
c1=97,c2=98

输出与 (1) 相同。

原因：
将 c1、c2 定义为 int 类型后，它们仍然是整型变量，赋值为 97 和 98。
- %c 输出时，取低字节（仍为 97 和 98），对应字符 'a' 和 'b'，结果不变。
- %d 输出时，直接输出 97 和 98，结果也不变。

若理解为在 (2) 的基础上将 char 改为 int（即 c1=197, c2=198 且类型为 int）：
输出：
c1=字符（197 对应字符）,c2=字符（198 对应字符）
c1=197,c2=198

原因：
int 类型可以完整存储 197 和 198，不会发生溢出，%c 输出对应字符，%d 输出原值。
*/

#include <stdio.h>

int main()
{
    int c1, c2;
    c1 = 97;
    c2 = 98;
    printf("c1=%c,c2=%c\n", c1, c2);
    printf("c1=%d,c2=%d\n", c1, c2);
    getchar();
    return 0;
}
