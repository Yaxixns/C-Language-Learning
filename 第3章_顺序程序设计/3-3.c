/*
 * 习题3
 * 购房从银行贷了一笔款 d，准备每月还款额为 p，月利率为 r，计算多少月能还清。
 * 设 d 为 300000 元，p 为 6000 元，r 为 1%。求得的月份取小数点后一位，
 * 对第 2 位按四舍五入处理。
 *
 * 提示：计算还清月数 m 的公式如下：
 *     m = log10(p/(p-d*r)) / log10(1+r)
 *
 * C 的库函数中有求对数的函数 log10，是以 10 为底的对数。
 */

#include <stdio.h>
#include <math.h>

int main()
{
    float d = 300000; // 贷款总额
    float p = 6000;   // 每月还款额
    float r = 0.01;   // 月利率 1%
    float m;

    m = log10(p / (p - d * r)) / log10(1 + r);

    printf("贷款 %.0f 元，每月还 %.0f 元，月利率 %.0f%%\n", d, p, r * 100);
    printf("需要 %.1f 个月还清\n", m);

    getchar();
    return 0;
}

/* ────────────────────────────────────────
 * 📝 批改评语：✅ 正确！10/10
 *
 * ① 还款月数公式 m = log10(p/(p-dr)) / log10(1+r) 使用正确。
 * ② d*r 为每月应付利息，p-d*r > 0 保证有解（6000>3000✓）。
 * ③ %.1f 自动四舍五入到小数点后一位，与题意一致。
 * ④ log10 需要 #include <math.h>，已正确包含。
 *
 * 验证：log10(2)/log10(1.01) ≈ 69.66 → 输出 69.7 个月 ✓
 * ──────────────────────────────────────── */
