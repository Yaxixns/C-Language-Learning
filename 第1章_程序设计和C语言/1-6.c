/*
 * 习题6
 * 编写一个 C 程序，运行时输入 a,b,c 三个值，输出其中值最大者。
*/

#include <stdio.h>

int main()
{
    int a, b, c;
    scanf("%d %d %d", &a, &b, &c);
    int max = a;
    if (b > max)
        max = b;
    if (c > max)
        max = c;
    printf("%d\n", max);
    getchar();
    return 0;
}

/* ────────────────────────────────────────
 * 📝 批改评语：✅ 正确！10/10
 *
 * ① 用"打擂台"方式求最大值，思路清晰。
 * ② if 语句使用正确，逐个比较更新max。
 * ③ scanf 正确使用了 & 取地址符。
 *
 * 💡 拓展思考（非扣分项）：
 *   - 也可以用三目运算符：max = (a>b?a:b) > c ? (a>b?a:b) : c;
 *   - getchar() 此处可能吞不到残留换行符，因为 scanf 会留下\n，
 *     建议考试时直接 return 0，或调试时用 system("pause")。
 *
 * 总评：逻辑正确，风格简洁，好的开始！
 * ──────────────────────────────────────── */