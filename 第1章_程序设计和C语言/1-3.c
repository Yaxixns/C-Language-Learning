/*
 * 习题3
 * 正确理解以下名词及其含义：
 * （1）源程序，目标程序，可执行程序。
 * （2）程序编辑，程序编译，程序连接。
 * （3）程序，程序模块，程序文件。
 * （4）函数，主函数，被调用函数，库函数。
 * （5）程序调试，程序测试。
*/

/*
 * 参考答案：
 *
 * （1）源程序：用高级语言（如C语言）编写的、人类可读的原始程序文件（.c文件）。
 *     目标程序：源程序经过编译后生成的二进制机器指令文件（.obj文件），
 *              不能直接运行。
 *     可执行程序：目标程序与库函数连接后生成的、计算机可直接运行的程序（.exe文件）。
 *
 * （2）程序编辑：在编辑器（IDE）中编写、修改源程序代码的过程。
 *     程序编译：编译器将源程序翻译成目标程序（机器指令），并检查语法错误。
 *     程序连接：连接器将目标程序与所需的库函数合并，生成可执行程序。
 *              口诀：编辑→编译→连接→运行。
 *
 * （3）程序：为解决某个问题而设计的指令集合。
 *     程序模块：将一个大型程序按功能拆分成若干个相对独立的部分，每部分为一个模块。
 *     程序文件：存储程序代码的文件（源文件 .c、头文件 .h、目标文件 .obj 等）。
 *
 * （4）函数：完成特定功能的独立代码块，是C程序的基本组成单元。
 *     主函数（main函数）：C程序的入口，程序从main开始执行。一个程序有且仅有一个main。
 *     被调用函数：被其他函数（包括main）调用的函数。
 *     库函数：C编译系统提供的标准函数（如printf、scanf、sqrt），包含在头文件中。
 *
 * （5）程序调试：发现、定位和修正程序中的语法错误和逻辑错误的过程。
 *     程序测试：用多组数据运行程序，验证程序是否满足所有功能需求。
 */