/*
 * 习题17
 * 用递归法将一个整数 n 转换成字符串。例如，输入 483，应输出字符串"483"。
 * n 的位数不确定，可以是任意位数的整数。
 */

#include <stdio.h>

// 递归转换并输出函数
void convert(int n)
{
    // 处理负数情况
    if (n < 0)
    {
        putchar('-');
        n = -n; // 转为正数处理
    }

    // 递归终止条件：当商为 0 时停止深入
    // 先通过 n / 10 剥离掉最后一位，把前面的部分送入下一层递归
    if (n / 10 != 0)
    {
        convert(n / 10);
    }

    // 回溯时执行：打印当前层的个位数
    // n % 10 得到数字，加上 '0' 的 ASCII 码值即可转换为字符
    putchar(n % 10 + '0');
}

int main()
{
    int n;

    printf("请输入一个整数: ");
    if (scanf("%d", &n) == 1)
    {
        printf("转换后的字符串为: ");
        convert(n);
        printf("\n");
    }

    return 0;
}
/* ✅ 10/10 递归整数转字符串正确！处理负数，先递归再输出高位，回溯顺序精准 */
