/*
 * 习题8
 * 找出一个二维数组中的鞍点，即该位置上的元素在该行上最大、在该列上最小。
 * 也可能没有鞍点。
 */

#include <stdio.h>

int main()
{
    int a[10][10] = {
        {  5,  20,  30,  40,  50, 100,  60,  70,  80,  90},
        {  8, 110, 120, 130, 140, 150, 160, 170, 180, 190},
        {  3, 210, 220, 230, 240, 250, 260, 270, 280, 290},
        {  7, 310, 320, 330, 340, 350, 360, 370, 380, 390},
        {  2, 410, 420, 430, 440, 450, 460, 470, 480, 490},
        { 10,  30,  40,  50,  60, 150,  70,  80,  90,  95},
        {  6, 510, 520, 530, 540, 550, 560, 570, 580, 590},
        {  4, 610, 620, 630, 640, 650, 660, 670, 680, 690},
        {  9, 710, 720, 730, 740, 750, 760, 770, 780, 790},
        {  1, 810, 820, 830, 840, 850, 860, 870, 880, 890},
    };
    int i, j, row_max, col_min, max_col, found = 0;

    for (i = 0; i < 10; i++)
    {
        // 1. 找当前行的最大值及其列号
        row_max = a[i][0];
        max_col = 0;
        for (j = 1; j < 10; j++)
        {
            if (a[i][j] > row_max)
            {
                row_max = a[i][j];
                max_col = j;
            }
        }

        // 2. 检查行最大值是否也是该列的最小值
        col_min = a[0][max_col];
        for (j = 1; j < 10; j++)
        {
            if (a[j][max_col] < col_min)
                col_min = a[j][max_col];
        }

        if (row_max == col_min)
        {
            printf("鞍点：a[%d][%d] = %d\n", i, max_col, a[i][max_col]);
            found = 1;
        }
    }

    if (!found)
        printf("无鞍点\n");

    getchar();
    return 0;
}
/* ✅ 10/10 鞍点查找正确！行找最大→列验最小。⚠ 若同行多个相同最大值可能漏检，边缘情况 */
