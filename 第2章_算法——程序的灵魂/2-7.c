/*
 * 习题7
 * 什么叫结构化程序设计？它的主要内容是什么？
*/

/*
 * 参考答案：
 *
 * 定义：结构化程序设计是一种程序设计方法，强调程序仅由三种基本结构
 * （顺序、选择、循环）组成，不使用或严格限制 goto 语句。
 *
 * 主要内容（四个要点）：
 * ① 自顶向下（Top-Down）：
 *    从总体目标出发，逐层分解为更小的子任务。
 * ② 逐步细化（Stepwise Refinement）：
 *    对每个模块不断细化其实现细节。
 * ③ 模块化设计（Modular Design）：
 *    将程序划分为功能独立的模块，通过接口通信。
 * ④ 结构化编码（Structured Coding）：
 *    仅用三种基本结构编写代码，避免 goto。
 *
 * 一句话：结构化程序设计 = 自顶向下 + 逐步细化 + 模块化 + 三种基本结构。
 */
