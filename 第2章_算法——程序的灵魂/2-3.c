/*
 * 习题3
 * 试述 3 种基本结构的特点，请另外设计两种基本结构（要符合基本结构的特点）。
*/

/*
 * 参考答案：
 *
 * 三种基本结构：
 * （1）顺序结构：按书写顺序依次执行，无分支、无跳转。
 * （2）选择结构：根据条件判断，选择执行两条路径之一（if-else）。
 * （3）循环结构：条件满足时重复执行某段代码（while / do-while / for）。
 *
 * 共同特点（必背！）：
 *   ① 只有一个入口；
 *   ② 只有一个出口；
 *   ③ 结构内的每一部分都有机会被执行到；
 *   ④ 结构内不存在"死循环"（即无终止的循环）。
 *
 * 另外设计的两种基本结构：
 *
 * （4）多分支选择结构（switch-case）：
 *     根据表达式值从多个路径中选择一条执行，所有分支汇聚到同一出口。
 *     满足单入口单出口、每部分可执行、无死循环的特点。
 *
 * （5）并行处理结构：
 *     多个相互独立的处理步骤依次执行，每个步骤都可能被执行到，
 *     整体只有一个入口和一个出口。类似流水线。
 */
