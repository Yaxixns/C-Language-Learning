/*
 * 习题1
 * 什么是算法？试从生活中找 3 个例子，描述它们的算法。
*/

/*
 * 参考答案：
 *
 * 算法：为解决一个问题而采取的方法和步骤的精确描述。
 * 它是程序的灵魂，具有有穷性、确定性、有效性、零个或多个输入、
 * 一个或多个输出五个基本特性。
 *
 * 生活例子：
 * ① 洗衣服的算法：
 *   步骤1：放入衣物和洗衣液 → 步骤2：选择洗涤模式 → 步骤3：启动
 *   → 步骤4：等待结束 → 步骤5：取出晾晒 → 结束。
 *
 * ② 乘地铁的算法：
 *   步骤1：进站刷卡 → 步骤2：安检 → 步骤3：找到站台
 *   → 步骤4：上车 → 步骤5：到站下车 → 步骤6：出站刷卡 → 结束。
 *
 * ③ 番茄炒蛋的算法：
 *   步骤1：番茄切块、鸡蛋打散 → 步骤2：热锅放油 → 步骤3：炒蛋盛出
 *   → 步骤4：炒番茄出汁 → 步骤5：混合翻炒 → 步骤6：加盐 → 结束。
 */
