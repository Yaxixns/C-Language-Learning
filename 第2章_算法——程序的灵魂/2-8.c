#include <stdio.h>

int main()
{
    int max, i, x;
    for (i = 0; i < 10; i++)
    {
        if (i == 0)
        {
            scanf("%d", &x);
            max = x;
        }
        else
        {
            scanf("%d", &x);
            if (x > max)
                max = x;
        }
    }

    printf("%d\n", max);
    getchar();
    return 0;
}

